public class Unary {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        System.out.println("**************************************************************");
        int b = 7;
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);

    }
}
